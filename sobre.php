	<h1>ILUMINA FÁCIL</h1>
	<br>
	<p>
		Ferramenta desenhada para fazer o cálculo de um projeto de iluminação. O usuário insere as medidas da sala ou do terreno (comprimento e largura), informa a quantidade de lâmpadas (se já tiver esta informação), informa a distância das lâmpadas até o quadro elétrico, a quantidade de tomadas e quantas delas são para eletrodomésticos especiais (eletrodomésticos de maior potência: refrigeradores, micro-ondas,... etc.). Como resultado o programa informa a potência que as lâmpadas deverão ter, o disjuntor para a alimentação do sistema e a bitola (diâmetro) do cabo a ser instalado.
	</p>
